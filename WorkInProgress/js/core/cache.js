"use strict";

class Cache {

    constructor() {

        this.items = new Map();

        this.stats = {

            hits: 0,

            misses: 0,

            writes: 0,

            deletes: 0

        };

    }

    /**************************************************************************
     SET
    **************************************************************************/

    set(key, value, ttl = Config.CACHE.TTL) {

        this.items.set(

            key,

            {

                value,

                expires:

                    Date.now() + ttl,

                created:

                    Date.now()

            }

        );

        this.stats.writes++;

    }

    /**************************************************************************
     GET
    **************************************************************************/

    get(key) {

        const item =

            this.items.get(key);

        if (!item) {

            this.stats.misses++;

            return null;

        }

        if (

            Date.now() >

            item.expires

        ) {

            this.items.delete(key);

            this.stats.misses++;

            return null;

        }

        this.stats.hits++;

        return item.value;

    }

    /**************************************************************************
     HAS
    **************************************************************************/

    has(key) {

        return this.get(key) !== null;

    }

    /**************************************************************************
     DELETE
    **************************************************************************/

    delete(key) {

        this.items.delete(key);

        this.stats.deletes++;

    }

    /**************************************************************************
     CLEAR
    **************************************************************************/

    clear() {

        this.items.clear();

    }

    /**************************************************************************
     TTL
    **************************************************************************/

    ttl(key) {

        const item =

            this.items.get(key);

        if (!item)

            return 0;

        return Math.max(

            0,

            item.expires -

            Date.now()

        );

    }

    /**************************************************************************
     CLEAN
    **************************************************************************/

    clean() {

        for (

            const [key,item]

            of this.items

        ) {

            if (

                Date.now() >

                item.expires

            ) {

                this.items.delete(key);

            }

        }

    }

    /**************************************************************************
     SIZE
    **************************************************************************/

    size() {

        return this.items.size;

    }

    /**************************************************************************
     STATS
    **************************************************************************/

    statistics() {

        return {

            size:

                this.size(),

            ...this.stats

        };

    }

    /**************************************************************************
     HEALTH
    **************************************************************************/

    healthCheck() {

        Logger.info(

            "Cache",

            this.statistics()

        );

    }

}

window.Cache = new Cache();