/******************************************************************************
 Bellacosa Mainframe Library
 Parser Service
 Version 2.0
******************************************************************************/

"use strict";

class ParserService {

    constructor() {

        this.version = Config.APP.VERSION;

    }

    /**************************************************************************
     INITIALIZE
    **************************************************************************/

    async initialize() {

        Logger.info(

            "Parser Service initialized."

        );

    }

    /**************************************************************************
     FEED
    **************************************************************************/

    parseFeed(feed) {

        if (!feed?.feed?.entry)

            return [];

        return feed.feed.entry.map(

            entry =>

                this.parseEntry(entry)

        );

    }

    /**************************************************************************
     ENTRY
    **************************************************************************/

    parseEntry(entry) {

        return {

            id:

                this.id(entry),

            title:

                this.title(entry),

            slug:

                this.slug(entry),

            url:

                this.url(entry),

            summary:

                this.summary(entry),

            content:

                this.content(entry),

            author:

                this.author(entry),

            published:

                this.published(entry),

            updated:

                this.updated(entry),

            categories:

                this.categories(entry),

            image:

                this.image(entry),

            readingTime:

                this.readingTime(entry),

            wordCount:

                this.wordCount(entry)

        };

    }

    /**************************************************************************
     ID
    **************************************************************************/

    id(entry) {

        return entry.id?.$t ?? "";

    }

    /**************************************************************************
     TITLE
    **************************************************************************/

    title(entry) {

        return entry.title?.$t ?? "";

    }

    /**************************************************************************
     SLUG
    **************************************************************************/

    slug(entry) {

        return Utils.slug(

            this.title(entry)

        );

    }

    /**************************************************************************
     URL
    **************************************************************************/

    url(entry) {

        return entry.link

            ?.find(

                link =>

                    link.rel === "alternate"

            )?.href ?? "";

    }

    /**************************************************************************
     SUMMARY
    **************************************************************************/

    summary(entry) {

        return entry.summary?.$t ?? "";

    }

    /**************************************************************************
     CONTENT
    **************************************************************************/

    content(entry) {

        return (

            entry.content?.$t ??

            entry.summary?.$t ??

            ""

        );

    }

    /**************************************************************************
     AUTHOR
    **************************************************************************/

    author(entry) {

        return entry.author

            ?.[0]

            ?.name

            ?.$t ?? "";

    }

    /**************************************************************************
     PUBLISHED
    **************************************************************************/

    published(entry) {

        return new Date(

            entry.published?.$t

        );

    }

    /**************************************************************************
     UPDATED
    **************************************************************************/

    updated(entry) {

        return new Date(

            entry.updated?.$t

        );

    }

    /**************************************************************************
     CATEGORIES
    **************************************************************************/

    categories(entry) {

        return entry.category

            ?.map(

                category =>

                    category.term

            ) ?? [];

    }

    /**************************************************************************
     IMAGE
    **************************************************************************/

    image(entry) {

        if (

            entry.media$thumbnail?.url

        ) {

            return entry.media$thumbnail.url

                .replace(

                    "/s72-c/",

                    "/s1200/"

                );

        }

        const html =

            this.content(entry);

        const image =

            html.match(

                /<img[^>]+src=["']([^"']+)["']/i

            );

        return image

            ? image[1]

            : Config.API.IMAGE_PLACEHOLDER;

    }

    /**************************************************************************
     TEXT
    **************************************************************************/

    text(entry) {

        return this.content(entry)

            .replace(

                Constants.REGEX.HTML,

                " "

            )

            .replace(

                Constants.REGEX.MULTIPLE_SPACES,

                " "

            )

            .trim();

    }

    /**************************************************************************
     WORD COUNT
    **************************************************************************/

    wordCount(entry) {

        const text =

            this.text(entry);

        if (!text)

            return 0;

        return text

            .split(" ")

            .length;

    }

    /**************************************************************************
     READING TIME
    **************************************************************************/

    readingTime(entry) {

        return Math.max(

            1,

            Math.ceil(

                this.wordCount(entry)

                / 200

            )

        );

    }

    /**************************************************************************
     EXCERPT
    **************************************************************************/

    excerpt(entry, size = 200) {

        return this.text(entry)

            .substring(0, size)

            .trim();

    }

    /**************************************************************************
     JSON
    **************************************************************************/

    export(posts) {

        return JSON.stringify(

            posts,

            null,

            2

        );

    }

    /**************************************************************************
     DIAGNOSTICS
    **************************************************************************/

    diagnostics() {

        return {

            service:

                "Parser",

            version:

                this.version

        };

    }

    /**************************************************************************
     HEALTH
    **************************************************************************/

    healthCheck() {

        Logger.group(

            "Parser Service"

        );

        Logger.table(

            this.diagnostics()

        );

        Logger.groupEnd();

    }

    /**************************************************************************
     VERSION
    **************************************************************************/

    versionInfo() {

        return this.version;

    }

}

/******************************************************************************
 EXPORT
******************************************************************************/

window.ParserService =

    new ParserService();